[SuppressMessage("ReSharper", "InconsistentNaming")]
public abstract partial class TypeGenerator : Generator
{
    public sealed record GeneratedFile(string FileName, string Source);

    private const string FileHeader = """
                                      //------------------------------------------------------------------------------
                                      // <auto-generated>
                                      //     This code was generated by a tool.
                                      //
                                      //     Changes to this file may cause incorrect behaviour and will be lost if
                                      //     the code is regenerated.
                                      // </auto-generated>
                                      //------------------------------------------------------------------------------
                                      #nullable enable

                                      """;

    [GeneratedRegex(@"\r?\n\r?\n\r?\n")]
    private static partial Regex DoubleBlankLinesRegex();

    [GeneratedRegex(@"{\r?\n\r?\n")]
    private static partial Regex ExtraBlankLineInClassDeclarationRegex();

    [GeneratedRegex(@"\r?\n")]
    private static partial Regex NewlineRegex();

    protected const string EmulatorFieldName = "emulator";

    private protected TypeGenerator()
    {
    }

